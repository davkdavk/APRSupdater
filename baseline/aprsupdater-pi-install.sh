#!/bin/bash
# APRS Updater - Self-contained Pi installer
# Just copy THIS ONE FILE to your Pi and run it!

set -e

echo "=== APRS Updater Pi Installer ==="
echo ""

# Extract main.go from this script
echo "Extracting source code..."
SCRIPT_LINE=$(grep -n "=== ENCODED MAIN.GO ===" "$0" | cut -d: -f1)
SCRIPT_LINE=$((SCRIPT_LINE + 1))
tail -n +${SCRIPT_LINE} "$0" | base64 -d > main.go
echo "Source extracted!"
ls -lh main.go
echo ""

# Check if Go is installed
if ! command -v go &> /dev/null; then
    echo "Go not found. Installing..."
    ARCH=$(uname -m)
    if [ "$ARCH" = "aarch64" ] || [ "$ARCH" = "arm64" ]; then
        GO_ARCH="arm64"
    else
        GO_ARCH="armv6l"
    fi
    wget -q "https://go.dev/dl/go1.21.6.linux-${GO_ARCH}.tar.gz"
    sudo tar -C /usr/local -xzf "go1.21.6.linux-${GO_ARCH}.tar.gz"
    export PATH=$PATH:/usr/local/go/bin
    echo "Go installed!"
else
    export PATH=$HOME/go/bin:/usr/local/go/bin:$PATH
fi

echo "Go version:"
go version
echo ""

# Install dependencies
echo "Installing dependencies..."
sudo apt-get update -qq
sudo apt-get install -y -qq xorg-dev libgl1-mesa-dev libxrandr-dev libxxf86vm-dev libxi-dev libxcursor-dev
echo "Dependencies installed!"
echo ""

# Build
echo "Building APRS Updater..."
CGO_ENABLED=1 go build -o aprsupdater-pi .
echo ""
echo "=== Build Complete! ==="
ls -lh aprsupdater-pi
echo ""
echo "To run: ./aprsupdater-pi"
echo ""

exit 0
